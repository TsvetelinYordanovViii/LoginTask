$('#facebook').hover(() => {
    $('#facebook .social-media-name').addClass('hoveredOver');
    $('#facebook .social-media-icon').addClass('hoveredOver');
}, () => {
    $('#facebook .social-media-name').removeClass('hoveredOver');
    $('#facebook .social-media-icon').removeClass('hoveredOver');
})

